                <h4 id="2-1-2-version">2.1.2 Version</h4>
                <p>required
                    <a href="http://nodejs.org/">node.js</a>&amp;
                    <a href="http://bower.io/">bower</a>&amp;
                    <a href="http://gruntjs.com/getting-started">grunt</a>
                </p>
                <pre><code>$ git clone https://github.com/onokumus/Bootstrap-Admin-Template.git yourfoldername
$ cd yourfoldername
$ git submodule init
$ git submodule update
$ npm install
$ bower install (./node_modules/bower/bin/bower install)
$ grunt (./node_modules/grunt-cli/bin/grunt)
$ cd dist/
$ open index.html</code></pre>
                <h4 id="1-2-version">1.2 Version</h4>
